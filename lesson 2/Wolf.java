public class Wolf {

    String name;
    String gender;
    String color;
    int age;
    int weight;

    public void go() {
        System.out.println(" Волк идет");
    }

    public void sit() {
        System.out.println(" Волк сидит");
    }

    public void run() {
        System.out.println(" Волк бежит");
    }

    public void hunt() {
        System.out.println(" Волк охотится");
    }

    public void howl() {
        System.out.println(" Волк воет");
    }
}